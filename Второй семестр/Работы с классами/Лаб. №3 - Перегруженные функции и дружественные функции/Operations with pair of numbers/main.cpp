#include <iostream>
#include "class_PAIR.h"
using namespace std;

int main() {
    system("chcp 1251");

    // Создание экземпляра класса с помощью конструктора по умолчанию
    Pair p1;
    cout << "Начальный объект Pair (по умолчанию): " << p1 << endl;

    // Создание экземпляра класса с заданными значениями
    Pair p2(5, 10.5);
    cout << "Объект Pair с заданными значениями: " << p2 << endl;

    // Копирование объекта Pair
    Pair p3 = p2;
    cout << "Объект Pair (скопированный): " << p3 << endl;

    // Префиксный декремент
    cout << "Префиксный декремент: " << --p3 << endl;

    // Постфиксный декремент
    cout << "Постфиксный декремент: " << p3-- << endl;
    cout << "После постфиксного декремента: " << p3 << endl;

    // Вычитание двух пар
    Pair p4 = p2 - p3;
    cout << "Вычитание пар: " << p2 << " - " << p3 << " = " << p4 << endl;

    // Добавление целого числа к паре
    Pair p5 = p2 + 3;
    cout << "Добавление целого числа: " << p2 << " + 3 = " << p5 << endl;

    // Добавление вещественного числа к паре
    Pair p6 = p2 + 3.5;
    cout << "Добавление вещественного числа: " << p2 << " + 3.5 = " << p6 << endl;

    // Ввод данных в объект Pair
    Pair p7;
    cout << "Введите значения для нового объекта Pair:" << endl;
    cin >> p7;
    cout << "Введенный объект Pair: " << p7 << endl;

    system("pause");
    return 0;
}